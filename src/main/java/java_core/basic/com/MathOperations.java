package java_core.basic.com;

public class MathOperations
{

	public static void main(String[] args) 
	{
		int x, y, answer;
		x = 20;
		y = 50;
		answer = x + y;
		System.out.println("Answer is --- " + "[" + answer + "]");
		
		int e, r;
		e = 10;
		r = 5;
		answer = e % r;
		System.out.println("Answer is --- " + "[" + answer + "]");
	}

}
